public class Calculator {
    public double doAdd(double x, double y)
    {
        return x + y;
    }

    public double doSub(double x, double y)
    {
        return x - y;
    }

    public double doMultiply(double x, double y)
    {
        return x * y;
    }

    public double doDiv(double x, double y)
    {
        return x / y;
    }
}
